#!/bin/bash

# ===============================================
# 大数据平台网络管理脚本
# 作用：创建和管理Docker网络，确保所有大数据组件在同一个网络内
# 原理：使用桥接网络模式，容器间通过容器名进行服务发现和通信
# 重要性：网络是分布式系统的基础，确保组件间正常通信
# ===============================================

# ===============================================
# 网络配置参数
# 作用：定义大数据平台使用的网络名称
# 原理：所有组件使用相同的网络名称，便于服务发现
# 网络模式：桥接网络，提供容器间通信能力
# ===============================================
NETWORK_NAME="bigdata-net"

# ===============================================
# 网络创建逻辑
# 作用：检查网络是否存在，如果不存在则创建
# 原理：幂等操作，避免重复创建网络导致错误
# 网络驱动：使用桥接驱动，适合容器间通信场景
# ===============================================

# 检查网络是否已存在
echo "检查大数据平台网络状态..."
if docker network ls | grep -q "$NETWORK_NAME"; then
    echo "✓ 网络 $NETWORK_NAME 已存在，无需创建"
else
    echo "创建网络 $NETWORK_NAME..."
    # 创建桥接网络，使用默认配置
    docker network create --driver bridge $NETWORK_NAME
    
    # 验证网络创建成功
    if [ $? -eq 0 ]; then
        echo "✓ 网络 $NETWORK_NAME 创建成功"
    else
        echo "✗ 网络创建失败，请检查Docker服务状态"
        exit 1
    fi
fi

# ===============================================
# 网络信息显示
# 作用：显示已创建网络的详细信息
# 原理：使用docker network inspect命令获取网络配置
# 显示内容：网络名称、子网、网关等关键信息
# ===============================================

echo ""
echo "网络详细信息:"
echo "================"
# 显示网络的基本配置信息
docker network inspect $NETWORK_NAME | grep -E "(Name|Subnet|Gateway)"

echo ""
echo "网络创建/检查完成，大数据平台网络已就绪"
echo "==========================================="